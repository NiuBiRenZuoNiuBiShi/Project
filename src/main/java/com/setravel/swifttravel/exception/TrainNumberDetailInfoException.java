package com.setravel.swifttravel.exception;

public class TrainNumberDetailInfoException extends Exception {
    public TrainNumberDetailInfoException() {
        super();
    }

    public TrainNumberDetailInfoException(String message) {
        super(message);
    }

    public TrainNumberDetailInfoException(String message, Throwable cause) {
        super(message, cause);
    }

    public TrainNumberDetailInfoException(Throwable cause) {
        super(cause);
    }
}
